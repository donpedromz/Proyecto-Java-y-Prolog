    public List<Symptom> getSelectedSymptoms() {
        List<Symptom> seleccionados = new ArrayList<>(); 
        DefaultTableModel tableModel = (DefaultTableModel) this.jTable2.getModel();
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            Boolean checked = (Boolean) tableModel.getValueAt(i, 2);
            if (checked) {
                SymptomModel s = modeledSymptoms.get(i);
                s.setSelected(true);
                seleccionados.add(modeledSymptoms.get(i).toSymptom(s));
            }
        }
        return seleccionados;
    }

    /**
     * Muestra los diagnósticos en la tabla reemplazando el modelo actual.
     */
    public void showDiagnostics(List<Diagnostic> diagnostics, List<Symptom> inputSymptoms) {
        this.currentDiagnostics = diagnostics;
        this.currentInputSymptoms = inputSymptoms;

        String[] cols = {"Id", "Enfermedad", "Categoria", "Sintomas", "Recomendaciones", "Sintomas ingresados"};
        DefaultTableModel model = new DefaultTableModel(cols, 0) {
            @Override
            public boolean isCellEditable(int row, int column) { return false; }
        };

        for (Diagnostic d : diagnostics) {
            String sintomasEnfermedad = joinSymptoms(d.getDisease().getSymptoms());
            String recomendaciones = joinRecs(d.getDisease().getRecomendations());
            String sintomasIngresados = joinSymptoms(inputSymptoms);
            model.addRow(new Object[]{
                d.getDisease().getId(),
                d.getDisease().getName(),
                d.getDisease().getCategory() != null ? d.getDisease().getCategory().getName() : "",
                sintomasEnfermedad,
                recomendaciones,
                sintomasIngresados
            });
        }

        jTable2.setModel(model);
        jTable2.setRowHeight(25);
    }

    private String joinSymptoms(List<Symptom> symptoms) {
        if (symptoms == null) return "";
        return symptoms.stream()
                .map(Symptom::getDescription)
                .reduce("", (a, b) -> a.isEmpty() ? b : a + ", " + b);
    }

    private String joinRecs(List<Recomendation> recs) {
        if (recs == null) return "";
        return recs.stream()
                .map(Recomendation::getDescription)
                .reduce("", (a, b) -> a.isEmpty() ? b : a + ", " + b);
    }
