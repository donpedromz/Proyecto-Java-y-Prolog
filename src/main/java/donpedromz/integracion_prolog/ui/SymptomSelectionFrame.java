    private void initSymptomModel(List<Symptom> symptoms) {
        List<SymptomModel> model = new ArrayList<>();
        for (Symptom s : symptoms) {
            model.add(new SymptomModel(s));
        }
        this.modeledSymptoms = model;
    }

    private void initDiseaseTable(List<Symptom> symptoms) {
        initSymptomModel(symptoms);
        String[] defaultColumns = {"Symptom ID", "Symptom", "Check"};
        DefaultTableModel tableModel = new DefaultTableModel(defaultColumns, 0) {
            @Override
            public Class<?> getColumnClass(int column) {
                switch (column) {
                    case 0:
                        return Integer.class;
                    case 1:
                        return String.class;
                    case 2:
                        return Boolean.class;
                    default:
                        return String.class;
                }
            }

            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 2;
            }
        };
        for (SymptomModel s : this.modeledSymptoms) {
            tableModel.addRow(new Object[]{
                s.getId(),
                s.getDescription(),
                s.getSelected()
            });
        }
        jTable2.setModel(tableModel);
        jTable2.setRowHeight(25);
        DefaultTableCellRenderer centrado = new DefaultTableCellRenderer();
        centrado.setHorizontalAlignment(SwingConstants.CENTER);
        jTable2.getColumnModel().getColumn(0).setCellRenderer(centrado);
        jTable2.getColumnModel().getColumn(0).setPreferredWidth(125);
        jTable2.getColumnModel().getColumn(1).setPreferredWidth(225);
        jTable2.getColumnModel().getColumn(2). setPreferredWidth(100);
    }

